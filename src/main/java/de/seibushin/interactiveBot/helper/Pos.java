package de.seibushin.interactiveBot.helper;

public class Pos {
    private double x;
    private double y;

    public Pos(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
